WLTP_CYCLES = [WLTP1, WLTP2, WLTP3]

# =============================================================================
# Constants
# =============================================================================
RHO_AIR = 1.1
g = 9.81

# =============================================================================
# Profile creation
# =============================================================================
def WltpProfile(veh_specs,
                cycle_chronology=(1, 2, 3)):
    """
    Combines WLTP cycles to create a power profile with given vehicle specs
    """
    wltp = []
    for cycle in cycle_chronology:
        wltp.extend(WLTP_CYCLES[cycle-1])

    time = range(len(wltp))
    accel = [0]
    for index, t in enumerate(time):
        if index:
            accel.append((wltp[index] - wltp[index-1])/(t - time[index-1]))

    friction = [veh_specs['Crr']*veh_specs['mass']*g if v != 0 else 0 for v in wltp]
    drag = [0.5*RHO_AIR*veh_specs['SCx']*v**2 for v in wltp]
    inertia = [veh_specs['mass']*a for a in accel]

    force = [ma + ff + fd for ma, ff, fd in zip(inertia, friction, drag)]

    power_profile = []
    for f, v, in zip(force, wltp):
        if f >= 0:
            power = -f*v/veh_specs['powertrain_efficiency']
        else:
            power = -veh_specs['charge_efficiency']*f*v/veh_specs['powertrain_efficiency']
        power_profile.append(power)

    return power_profile, time
